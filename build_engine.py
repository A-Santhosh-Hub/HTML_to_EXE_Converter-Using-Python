class BuildEngine:

    # ── Project scanner ───────────────────────────────────────────────────────

    def scan_project(self, folder: str) -> str:
        """Return a tree-style string describing the project structure."""
        lines = []
        has_index = os.path.exists(os.path.join(folder, "index.html"))
        root_name = os.path.basename(folder)
        lines.append(f"📁 {root_name}/")

        stats = {"files": 0, "dirs": 0, "size_kb": 0}
        issues = []

        if not has_index:
            issues.append("⚠  index.html not found! Build will fail.")

        for root, dirs, files in os.walk(folder):
            dirs.sort()
            depth = root.replace(folder, "").count(os.sep)
            indent = "   " * depth
            if depth > 0:
                dname = os.path.basename(root)
                lines.append(f"{indent}📂 {dname}/")
                stats["dirs"] += 1
            for fname in sorted(files)[:20]:   # cap display at 20 per folder
                fpath = os.path.join(root, fname)
                size_kb = os.path.getsize(fpath) / 1024
                stats["files"] += 1
                stats["size_kb"] += size_kb
                icon = self._file_icon(fname)
                lines.append(f"{indent}   {icon} {fname}  ({size_kb:.1f} KB)")
            if len(files) > 20:
                lines.append(f"{indent}   … +{len(files)-20} more files")

        lines.append("")
        lines.append(f"📊  {stats['files']} files · {stats['dirs']} dirs · "
                      f"{stats['size_kb']:.1f} KB total")
        if has_index:
            lines.append("✔  index.html detected — ready to build")
        for issue in issues:
            lines.append(issue)

        return "\n".join(lines)

    @staticmethod
    def _file_icon(name: str) -> str:
        ext = Path(name).suffix.lower()
        icons = {
            ".html": "🌐", ".htm": "🌐",
            ".css":  "🎨",
            ".js":   "⚡", ".mjs": "⚡",
            ".json": "📋",
            ".png":  "🖼", ".jpg": "🖼", ".jpeg": "🖼",
            ".gif":  "🖼", ".svg": "🖼", ".webp": "🖼",
            ".mp4":  "🎬", ".webm": "🎬",
            ".mp3":  "🎵", ".wav": "🎵",
            ".ttf":  "🔤", ".woff": "🔤", ".woff2": "🔤",
            ".ico":  "🔷",
            ".pdf":  "📄",
            ".zip":  "📦",
        }
        return icons.get(ext, "📄")

    # ── Validation ────────────────────────────────────────────────────────────

    def validate(self, input_folder: str) -> list:
        """Return list of error strings (empty = OK)."""
        errors = []
        if not input_folder or not os.path.isdir(input_folder):
            errors.append("Input folder does not exist.")
            return errors
        if not os.path.exists(os.path.join(input_folder, "index.html")):
            errors.append("index.html not found in input folder.")
        return errors

    # ── Icon conversion ───────────────────────────────────────────────────────

    def _prepare_icon(self, icon_path: str, work_dir: str) -> Optional[str]:
        """Convert icon to .ico if needed. Returns path to .ico or None."""
        if not icon_path or not os.path.exists(icon_path):
            return None

        ext = Path(icon_path).suffix.lower()
        ico_dest = os.path.join(work_dir, "app_icon.ico")

        if ext == ".ico":
            shutil.copy2(icon_path, ico_dest)
            return ico_dest

        if not PILLOW_OK:
            return None  # Can't convert without Pillow

        try:
            img = PILImage.open(icon_path).convert("RGBA")
            sizes = [(16,16),(32,32),(48,48),(64,64),(128,128),(256,256)]
            img.save(ico_dest, format="ICO", sizes=sizes)
            return ico_dest
        except Exception:
            return None

    # ── Main build orchestrator ───────────────────────────────────────────────

    def build(self, cfg: dict, progress: Callable[[float, str], None]) -> str:
        """
        Full build pipeline.
        Returns the path to the output .exe on success.
        Raises RuntimeError on failure.
        """
        log_lines = []

        def log(msg: str):
            log_lines.append(f"[{datetime.now().strftime('%H:%M:%S')}] {msg}")

        # ── Validate ──────────────────────────────────────────────────────────
        progress(0.02, "Validating project structure…")
        errors = self.validate(cfg["input_folder"])
        if errors:
            raise RuntimeError("\n".join(errors))
        log("✔ Project validation passed")

        # ── Create temp working directory ──────────────────────────────────────
        progress(0.05, "Preparing build workspace…")
        build_dir = tempfile.mkdtemp(prefix="san_build_")
        log(f"  Work dir: {build_dir}")

        try:
            self._do_build(cfg, build_dir, progress, log)
        except Exception:
            # Write partial log before re-raising
            self._write_log(cfg["output_folder"], log_lines)
            raise
        finally:
            # Clean up temp dir (keep on error for debugging)
            try:
                shutil.rmtree(build_dir, ignore_errors=True)
            except Exception:
                pass

        out_exe = os.path.join(cfg["output_folder"],
                               cfg["app_name"].replace(" ", "_") + ".exe")
        self._write_log(cfg["output_folder"], log_lines)
        return out_exe

    def _do_build(self, cfg: dict, build_dir: str,
                  progress: Callable, log: Callable):

        app_name    = cfg["app_name"].replace(" ", "_")
        input_dir   = cfg["input_folder"]
        output_dir  = cfg["output_folder"]
        os.makedirs(output_dir, exist_ok=True)

        # ── Copy web project ──────────────────────────────────────────────────
        progress(0.10, "Copying HTML project assets…")
        web_project_dest = os.path.join(build_dir, "web_project")
        shutil.copytree(input_dir, web_project_dest, dirs_exist_ok=True)
        asset_count = sum(len(f) for _, _, f in os.walk(web_project_dest))
        log(f"  Copied {asset_count} assets")

        # ── Generate runtime_main.py ──────────────────────────────────────────
        progress(0.20, "Generating runtime engine…")
        runtime_code = RUNTIME_TEMPLATE
        replacements = {
            "{{APP_NAME}}":       cfg["app_name"],
            "{{APP_VERSION}}":    cfg.get("version", "1.0.0"),
            "{{APP_WIDTH}}":      str(cfg.get("width", 1200)),
            "{{APP_HEIGHT}}":     str(cfg.get("height", 800)),
            "{{FULLSCREEN}}":     str(cfg.get("fullscreen", False)),
            "{{RESIZABLE}}":      str(cfg.get("resizable", True)),
            "{{DEVTOOLS}}":       str(cfg.get("devtools", False)),
            "{{SPLASH_ENABLED}}": str(cfg.get("splash_enabled", True)),
            "{{SPLASH_DURATION}}":str(cfg.get("splash_duration", 2500)),
        }
        for k, v in replacements.items():
            runtime_code = runtime_code.replace(k, v)

        runtime_path = os.path.join(build_dir, "runtime_main.py")
        with open(runtime_path, "w", encoding="utf-8") as f:
            f.write(runtime_code)
        log("  Runtime engine generated")

        # ── Prepare icon ──────────────────────────────────────────────────────
        progress(0.28, "Processing icon…")
        icon_path = self._prepare_icon(cfg.get("icon", ""), build_dir)
        if icon_path:
            log(f"  Icon: {os.path.basename(icon_path)}")
        else:
            log("  No icon set (default PyInstaller icon will be used)")

        # ── Write runtime config JSON ─────────────────────────────────────────
        progress(0.32, "Writing runtime config…")
        rt_cfg = {
            "app_name":    cfg["app_name"],
            "version":     cfg.get("version", "1.0.0"),
            "author":      "Developed by Santhosh A",
            "author_url":  AUTHOR_URL,
        }
        with open(os.path.join(build_dir, "app_config.json"), "w") as f:
            json.dump(rt_cfg, f, indent=2)

        # ── Run PyInstaller ───────────────────────────────────────────────────
        progress(0.38, "Running PyInstaller… (this may take 1–3 minutes)")

        sep = ";" if sys.platform == "win32" else ":"

        cmd = [
            sys.executable, "-m", "PyInstaller",
            "--onefile",
            "--noconsole",
            "--clean",
            f"--name={app_name}",
            f"--distpath={output_dir}",
            f"--workpath={os.path.join(build_dir, 'pyinstaller_work')}",
            f"--specpath={build_dir}",
            f"--add-data={web_project_dest}{sep}web_project",
            f"--add-data={os.path.join(build_dir, 'app_config.json')}{sep}.",
        ]

        if icon_path and os.path.exists(icon_path):
            cmd.append(f"--icon={icon_path}")

        # Hidden imports that PyInstaller often misses
        hidden = [
            "webview",
            "webview.platforms.winforms",
            "clr",
        ]
        for h in hidden:
            cmd += ["--hidden-import", h]

        cmd.append(runtime_path)

        log(f"  Command: {' '.join(cmd[:6])} …")

        proc = subprocess.Popen(
            cmd,
            stdout=subprocess.PIPE,
            stderr=subprocess.STDOUT,
            cwd=build_dir,
            text=True,
            encoding="utf-8",
            errors="replace",
        )

        # Stream PyInstaller output
        pi_progress = 0.38
        while True:
            line = proc.stdout.readline()
            if not line and proc.poll() is not None:
                break
            line = line.rstrip()
            if line:
                log(f"  [PyInstaller] {line}")
                # Rough progress estimation
                if "Analyzing" in line:
                    pi_progress = min(0.55, pi_progress + 0.01)
                elif "Collecting" in line:
                    pi_progress = min(0.65, pi_progress + 0.005)
                elif "Building" in line:
                    pi_progress = min(0.80, pi_progress + 0.01)
                elif "PKG" in line or "EXE" in line:
                    pi_progress = min(0.92, pi_progress + 0.02)
                progress(pi_progress, f"PyInstaller: {line[:60]}…")

        rc = proc.wait()
        if rc != 0:
            raise RuntimeError(f"PyInstaller exited with code {rc}. "
                                f"Check build log for details.")

        progress(0.95, "Verifying output…")

        out_exe = os.path.join(output_dir, f"{app_name}.exe")
        if not os.path.exists(out_exe):
            # Try without extension (Linux/Mac)
            alt = os.path.join(output_dir, app_name)
            if os.path.exists(alt):
                out_exe = alt
            else:
                raise RuntimeError("Build seemed to succeed but .exe not found "
                                    f"in {output_dir}")

        size_mb = os.path.getsize(out_exe) / (1024 * 1024)
        log(f"  Output: {out_exe} ({size_mb:.1f} MB)")
        progress(1.0, f"✔ Done — {app_name}.exe ({size_mb:.1f} MB)")

    # ── Log writer ────────────────────────────────────────────────────────────

    @staticmethod
    def _write_log(output_dir: str, lines: list):
        try:
            os.makedirs(output_dir, exist_ok=True)
            log_path = os.path.join(output_dir, "build_log.txt")
            with open(log_path, "w", encoding="utf-8") as f:
                f.write("SanStudio HTML → EXE Converter — Build Log\n")
                f.write("Developed by Santhosh A\n")
                f.write(f"Generated: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n")
                f.write("=" * 60 + "\n\n")
                f.write("\n".join(lines))
                f.write("\n")
        except Exception:
            pass
