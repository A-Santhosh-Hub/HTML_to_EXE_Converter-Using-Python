# ── Find a free TCP port ──────────────────────────────────────────────────────
def _free_port() -> int:
    with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as s:
        s.bind(("127.0.0.1", 0))
        return s.getsockname()[1]

PORT = _free_port()

# ── Silent HTTP server ────────────────────────────────────────────────────────
class _Handler(http.server.SimpleHTTPRequestHandler):
    def __init__(self, *args, **kwargs):
        super().__init__(*args, directory=PROJECT_DIR, **kwargs)

    def log_message(self, fmt, *args):
        pass  # Silence all output

    def end_headers(self):
        # CORS + cache headers so page works like a real local server
        self.send_header("Access-Control-Allow-Origin", "*")
        self.send_header("Cache-Control", "no-cache, no-store, must-revalidate")
        super().end_headers()

def _run_server():
    with socketserver.TCPServer(("127.0.0.1", PORT), _Handler) as httpd:
        httpd.serve_forever()

# ── JS ↔ Python bridge ────────────────────────────────────────────────────────
class _Api:
    def open_url(self, url: str):
        """Open an external URL in the system browser."""
        webbrowser.open(url)

    def open_author(self):
        """Open author portfolio page."""
        webbrowser.open(AUTHOR_URL)

    def get_app_info(self) -> str:
        return json.dumps({
            "name":    APP_NAME,
            "version": APP_VERSION,
            "author":  AUTHOR,
        })

    def save_file(self, filename: str, data: str) -> str:
        """Save a text/data-URI file to the user's Downloads folder."""
        try:
            import base64, re
            downloads = os.path.join(os.path.expanduser("~"), "Downloads")
            os.makedirs(downloads, exist_ok=True)
            dest = os.path.join(downloads, filename)
            # Handle data URI
            match = re.match(r"data:[^;]+;base64,(.+)", data)
            if match:
                with open(dest, "wb") as f:
                    f.write(base64.b64decode(match.group(1)))
            else:
                with open(dest, "w", encoding="utf-8") as f:
                    f.write(data)
            return json.dumps({"ok": True, "path": dest})
        except Exception as e:
            return json.dumps({"ok": False, "error": str(e)})

